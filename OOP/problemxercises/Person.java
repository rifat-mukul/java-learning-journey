
public class Person {

    private String nameString;
    private int age;

    public Person (String nameString, int age) {
        this.nameString = nameString;
        this.age = age;

    }

    public String getName() {
        return nameString;
    }

    public void setName(String nameString) {
        this.nameString = nameString;
    }

    public int getAge() {
        return age;
    }

    public void setAge (int age) {
        this.age = age;
    }

    
    
}
