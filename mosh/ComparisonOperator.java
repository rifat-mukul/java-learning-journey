public class ComparisonOperator {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;
        int z = 10;

        System.out.println(x == y); //bollien expression 
        System.out.println(x != y);
        System.out.println(x > z);
        System.out.println(z > x);
    }
}
